package kr.co.with.studygroup.exam.vo;

public class StudyGroupExampleItemVO {
	private int itemNo;
	private int examNo;
	private int exampleNo;
	private String itemTitle;
	private String itemCorrect;
	public int getItemNo() {
		return itemNo;
	}
	public void setItemNo(int itemNo) {
		this.itemNo = itemNo;
	}
	public int getExamNo() {
		return examNo;
	}
	public void setExamNo(int examNo) {
		this.examNo = examNo;
	}
	public int getExampleNo() {
		return exampleNo;
	}
	public void setExampleNo(int exampleNo) {
		this.exampleNo = exampleNo;
	}
	public String getItemTitle() {
		return itemTitle;
	}
	public void setItemTitle(String itemTitle) {
		this.itemTitle = itemTitle;
	}
	public String getItemCorrect() {
		return itemCorrect;
	}
	public void setItemCorrect(String itemCorrect) {
		this.itemCorrect = itemCorrect;
	}

	
}
