package kr.co.with.studygroup.exam.vo;

public class StudyGroupExampleVO {
	private int exampleNo;
	private int examNo;
	private String exampleTitle;
	private String exampleDate;

	public int getExampleNo() {
		return exampleNo;
	}

	public void setExampleNo(int exampleNo) {
		this.exampleNo = exampleNo;
	}

	public int getExamNo() {
		return examNo;
	}

	public void setExamNo(int examNo) {
		this.examNo = examNo;
	}

	public String getExampleTitle() {
		return exampleTitle;
	}

	public void setExampleTitle(String exampleTitle) {
		this.exampleTitle = exampleTitle;
	}

	public String getExampleDate() {
		return exampleDate;
	}

	public void setExampleDate(String exampleDate) {
		this.exampleDate = exampleDate;
	}

}
